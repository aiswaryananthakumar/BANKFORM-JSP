package com.skilljobbie.bankformjsp;

public class BankBean {
	
	    private String name, email, phone, gender, dob, address, city, state, country, pincode;
	    private String accountType, aadhar, pan, nominee, deposit;

	    public String getName() { return name; }
	    public void setName(String name) { this.name = name; }

	    public String getEmail() { return email; }
	    public void setEmail(String email) { this.email = email; }

	    public String getPhone() { return phone; }
	    public void setPhone(String phone) { this.phone = phone; }

	    public String getGender() { return gender; }
	    public void setGender(String gender) { this.gender = gender; }

	    public String getDob() { return dob; }
	    public void setDob(String dob) { this.dob = dob; }

	    public String getAddress() { return address; }
	    public void setAddress(String address) { this.address = address; }

	    public String getCity() { return city; }
	    public void setCity(String city) { this.city = city; }

	    public String getState() { return state; }
	    public void setState(String state) { this.state = state; }

	    public String getCountry() { return country; }
	    public void setCountry(String country) { this.country = country; }

	    public String getPincode() { return pincode; }
	    public void setPincode(String pincode) { this.pincode = pincode; }

	    public String getAccountType() { return accountType; }
	    public void setAccountType(String accountType) { this.accountType = accountType; }

	    public String getAadhar() { return aadhar; }
	    public void setAadhar(String aadhar) { this.aadhar = aadhar; }

	    public String getPan() { return pan; }
	    public void setPan(String pan) { this.pan = pan; }

	    public String getNominee() { return nominee; }
	    public void setNominee(String nominee) { this.nominee = nominee; }

	    public String getDeposit() { return deposit; }
	    public void setDeposit(String deposit) { this.deposit = deposit; }
	}
